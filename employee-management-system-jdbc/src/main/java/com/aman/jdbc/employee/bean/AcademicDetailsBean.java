public class AcademicDetailsBean {

	private int empId;
	private String empDegree;
	private int empPassYear;
	private float empPercentage;

	public int getEmpId() {
		return empId;
	}

	public void setEmpId(int empId) {
		this.empId = empId;
	}

	public String getEmpDegree() {
		return empDegree;
	}

	public void setEmpDegree(String empDegree) {
		this.empDegree = empDegree;
	}

	public int getEmpPassYear() {
		return empPassYear;
	}

	public void setEmpPassYear(int empPassYear) {
		this.empPassYear = empPassYear;
	}

	public float getEmpPercentage() {
		return empPercentage;
	}

	public void setEmpPercentage(float empPercentage) {
		this.empPercentage = empPercentage;
	}
}
